require 'spec_helper'

module IsbnTool
  describe IsbnHyphenatizor do
    [
     ["9780966955309", "978-0-9669553-0-9"],
     ["9780201101799", "978-0-201-10179-9"],
     ["9783847390688", "978-3-8473-9068-8"],
     ["9783847395720", "978-3-8473-9572-0"],
     ["9783659986185", "978-3-659-98618-5"],
     ["9783639514971", "978-3-639-51497-1"],
     ["9783639514957", "978-3-639-51495-7"],
     ["9783639515152", "978-3-639-51515-2"],
     ["9783659023606", "978-3-659-02360-6"],
     ["9783659068058", "978-3-659-06805-8"],
     ["9783659074646", "978-3-659-07464-6"],
     ["9783659077647", "978-3-659-07764-7"]].each do |input, expected|
       it "hyphenates #{input}" do
         isbn = Isbn.new(input)
         hyphenatizor = IsbnHyphenatizor.new(isbn)
         expect(hyphenatizor.hyphenate).to eql(expected)
       end
     end
  end
end
